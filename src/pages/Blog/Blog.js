const Blog = ({ id }) => {
  const [isLoadedData, setIsLoadedData] = useState(false);
  const [isLoading, setIsLoading] = useState(false);
  const [blogItems, setBlogItems] = useState(dummyData);

  const handleLoadImages = () => {
    setIsLoading(true);
    setTimeout(() => {
      setBlogItems((items) => [...items, ...LoadedDummyData]);
      setIsLoadedData(true);
      setIsLoading(false);
    }, 700);
  };
  return (
    <Section id={id}>
      <LineTitle>
        <Button>{id}</Button>
      </LineTitle>
      <ImagesWrapper>
        {blogItems.map((blogItem) => (
          <BlogCard key={blogItem.id} {...blogItem} />
        ))}
      </ImagesWrapper>
      {isLoading && <LoadingIndicator />}
      {!isLoading && !isLoadedData && (
        <BackgroundButton onClick={handleLoadImages}>
          Load more
        </BackgroundButton>
      )}
    </Section>
  );
};

export default Blog;
